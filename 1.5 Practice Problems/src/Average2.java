public class Average2 {
    /*
    Given two numbers, num1 and num2, output the average.
    */
    public static void main(String[] args) {
        double num1 = 12;
        double num2 = 18;

        double addedSum = num1 + num2;

        double average = addedSum/2;

        System.out.println("The average is " + average);
    }



}



